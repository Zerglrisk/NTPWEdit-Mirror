#include <stdio.h>
#include <stdarg.h>
#include "log.h"

void WriteLog(char *format, ...)
    {
#ifdef DEBUG
    static FILE *log;
    va_list va;

    log=fopen("ntpwedit.log", "a");
    if(NULL==log)
        return;
    va_start(va, format);
    vfprintf(log, format, va);
    fclose(log);
    va_end(va);
#endif
    }
